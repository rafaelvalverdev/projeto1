# Exercício de contagem regressiva com a estrutura de repetição for:
from time import sleep  # Importando intervalo da biblioteca sleep

for count in range(10, 0, -1):  # Estrutura de repetição de contagem regressiva(-1)
    sleep(1)
    print(count)
sleep(1)
print('BUM, BUUM, POW! Feliz ano novo!!')
